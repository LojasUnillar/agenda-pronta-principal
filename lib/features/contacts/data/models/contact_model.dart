import '../../domain/entities/contact_entity.dart';

/// Model (DTO) para contato, responsável pela serialização.
///
/// Estende ContactEntity adicionando métodos de conversão
/// para comunicação com o Supabase.
class ContactModel extends ContactEntity {
  const ContactModel({
    required String id,
    required String name,
    String? socialName,
    required String status,
    DateTime? lastUpdate,
    String? imageUrl,
    String? email,
    String? cnpj,
    String? type,
    List<String> brandIds = const [],
    List<String> productIds = const [],
    int usageCount = 0,
    bool isFavorite = false,
    String? about,
    List<String> departments = const [],
    String? representative,
    List<String> representativeIds = const [],
    String? turnoverCode,
    String? minDeliveryTime,
    String? billingTime,
    int? tenureValue,
    String? tenureUnit,
    bool hasAssistance = false,
    List<String> assistancePhones = const [],
    List<String> devolucaoPhones = const [],
    List<String> financeiroPhones = const [],
    List<String> outroPhones = const [],
    String? outroLabel,
    bool hasInvoice = false,
    String? mainSite,
    bool hasBoletoSite = false,
    String? boletoSiteLink,
    String? regionOfActivity,
    String? birthday,
    String? personality,
    bool? isMarried,
    String? spouseName,
    bool? hasChildren,
    String? childrenNames,
    String? marketTime,
    String? address,
  }) : super(
          id: id,
          name: name,
          socialName: socialName,
          status: status,
          lastUpdate: lastUpdate,
          imageUrl: imageUrl,
          email: email,
          cnpj: cnpj,
          type: type,
          brandIds: brandIds,
          productIds: productIds,
          usageCount: usageCount,
          isFavorite: isFavorite,
          about: about,
          departments: departments,
          representative: representative,
          representativeIds: representativeIds,
          turnoverCode: turnoverCode,
          minDeliveryTime: minDeliveryTime,
          billingTime: billingTime,
          tenureValue: tenureValue,
          tenureUnit: tenureUnit,
          hasAssistance: hasAssistance,
          assistancePhones: assistancePhones,
          devolucaoPhones: devolucaoPhones,
          financeiroPhones: financeiroPhones,
          outroPhones: outroPhones,
          outroLabel: outroLabel,
          hasInvoice: hasInvoice,
          mainSite: mainSite,
          hasBoletoSite: hasBoletoSite,
          boletoSiteLink: boletoSiteLink,
          regionOfActivity: regionOfActivity,
          birthday: birthday,
          personality: personality,
          isMarried: isMarried,
          spouseName: spouseName,
          hasChildren: hasChildren,
          childrenNames: childrenNames,
          marketTime: marketTime,
          address: address,
        );

  /// Converte uma entidade para modelo
  factory ContactModel.fromEntity(ContactEntity entity) {
    return ContactModel(
      id: entity.id,
      name: entity.name,
      socialName: entity.socialName,
      status: entity.status,
      lastUpdate: entity.lastUpdate,
      imageUrl: entity.imageUrl,
      email: entity.email,
      cnpj: entity.cnpj,
      type: entity.type,
      brandIds: entity.brandIds,
      productIds: entity.productIds,
      usageCount: entity.usageCount,
      isFavorite: entity.isFavorite,
      about: entity.about,
      departments: entity.departments,
      representative: entity.representative,
      representativeIds: entity.representativeIds,
      turnoverCode: entity.turnoverCode,
      minDeliveryTime: entity.minDeliveryTime,
      billingTime: entity.billingTime,
      tenureValue: entity.tenureValue,
      tenureUnit: entity.tenureUnit,
      hasAssistance: entity.hasAssistance,
      assistancePhones: entity.assistancePhones,
      devolucaoPhones: entity.devolucaoPhones,
      financeiroPhones: entity.financeiroPhones,
      outroPhones: entity.outroPhones,
      outroLabel: entity.outroLabel,
      hasInvoice: entity.hasInvoice,
      mainSite: entity.mainSite,
      hasBoletoSite: entity.hasBoletoSite,
      boletoSiteLink: entity.boletoSiteLink,
      regionOfActivity: entity.regionOfActivity,
      birthday: entity.birthday,
      personality: entity.personality,
      isMarried: entity.isMarried,
      spouseName: entity.spouseName,
      hasChildren: entity.hasChildren,
      childrenNames: entity.childrenNames,
      marketTime: entity.marketTime,
      address: entity.address,
    );
  }

  /// Converte para mapa (JSON para Supabase)
  Map<String, dynamic> toMap() {
    return {
      if (id.isNotEmpty) 'id': id,
      'nome_fantasia': name,
      'razao_social': socialName,
      'cnpj': cnpj,
      'tipo_contato': type,
      'email': email,
      'departamentos': departments,
      'representantes': representativeIds,
      'marcas': brandIds,
      'produtos': productIds,
      'info_adicional': about,
      'is_actived': status == 'Ativo',
      'codigo_erp': turnoverCode?.isNotEmpty == true ? turnoverCode : null,
      'tempo_entrega': minDeliveryTime,
      'tempo_faturamento': billingTime,
      'tempo_casa': tenureValue != null ? '\$tenureValue \$tenureUnit' : null,
      'tem_assistencia': hasAssistance,
      'num_assistencia': assistancePhones.join('; '),
      'num_devolucao': devolucaoPhones.join('; '),
      'num_financeiro': financeiroPhones.join('; '),
      'num_outro': outroPhones.join('; '),
      'outro_label': outroLabel,
      'tem_nota': hasInvoice,
      'emite_nota_fiscal': hasInvoice,
      'site': mainSite,
      'tem_boleto': hasBoletoSite,
      'site_boleto': boletoSiteLink,
      'reg_atuacao': regionOfActivity,
      'data_aniversario': birthday,
      'personalidade': personality,
      'casado': isMarried,
      'nome_conjugue': spouseName,
      'tem_filhos': hasChildren,
      'nome_filhos': childrenNames,
      'tempo_mercado': marketTime,
      'endereco': address,
    };
  }

  /// Cria a partir de um mapa (JSON do Supabase)
  factory ContactModel.fromMap(Map<String, dynamic> map) {
    int? tValue;
    String? tUnit;
    if (map['tempo_casa'] != null) {
      final parts = (map['tempo_casa'] as String).split(' ');
      if (parts.isNotEmpty) {
        tValue = int.tryParse(parts[0]);
        if (parts.length > 1) tUnit = parts[1];
      }
    }

    return ContactModel(
      id: map['id'] ?? '',
      name: map['nome_fantasia'] ?? 'Sem Nome',
      socialName: map['razao_social'],
      status: (map['is_actived'] ?? true) ? 'Ativo' : 'Inativo',
      lastUpdate: map['update_at'] != null
          ? DateTime.tryParse(map['update_at'].toString())
          : null,
      imageUrl: map['logo_url'],
      email: map['email'],
      cnpj: map['cnpj'],
      type: map['tipo_contato'],
      brandIds: map['marcas'] != null ? List<String>.from(map['marcas']) : [],
      productIds:
          map['produtos'] != null ? List<String>.from(map['produtos']) : [],
      usageCount: 0,
      isFavorite: false,
      about: map['info_adicional'],
      departments: map['departamentos'] != null
          ? List<String>.from(map['departamentos'])
          : [],
      representative: null,
      representativeIds: map['representantes'] != null
          ? List<String>.from(map['representantes'])
          : [],
      turnoverCode: map['codigo_erp'],
      minDeliveryTime: map['tempo_entrega'],
      billingTime: map['tempo_faturamento'],
      tenureValue: tValue,
      tenureUnit: tUnit,
      hasAssistance: map['tem_assistencia'] ?? false,
      assistancePhones: (map['num_assistencia'] as String?)?.split('; ') ?? [],
      devolucaoPhones: (map['num_devolucao'] as String?)?.split('; ') ?? [],
      financeiroPhones: (map['num_financeiro'] as String?)?.split('; ') ?? [],
      outroPhones: (map['num_outro'] as String?)?.split('; ') ?? [],
      outroLabel: map['outro_label'],
      hasInvoice: map['emite_nota_fiscal'] ?? map['tem_nota'] ?? false,
      mainSite: map['site'],
      hasBoletoSite: map['tem_boleto'] ?? false,
      boletoSiteLink: map['site_boleto'],
      regionOfActivity: map['reg_atuacao'],
      birthday: map['data_aniversario'],
      personality: map['personalidade'],
      isMarried: map['casado'],
      spouseName: map['nome_conjugue'],
      hasChildren: map['tem_filhos'],
      childrenNames: map['nome_filhos'],
      marketTime: map['tempo_mercado'],
      address: map['endereco'],
    );
  }
}
